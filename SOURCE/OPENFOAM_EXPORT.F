      PROGRAM OPENFOAM_EXPORT
C
C     WRITTEN BY: MATTHEW STADELMAN
C     PROGRAM DESCRIPTON: OUTPUTS A BLOCK MESH DICT TO BE READ IN BY 
C     OPEN FOAM, WILL BE EVENTUALLY TURNED INTO A COMPONENT OF THE 
C     PYTHON MODEL TOOLS
C
C ---------------------------------------------------------------------
C     VARIABLE DESCRIPTION:
C     BOK - LOGICAL CONTROL VARIABLE
C
C ----------------------------------------------------------------------
C   
      USE TWOD_MODULE 
C      
      IMPLICIT NONE
      LOGICAL :: BOK
C
C     INITIALIZING THE MODEL 
      BOK   = .TRUE.
      CALL INITIALIZE_EXPORT(BOK)
      IF (.NOT. BOK) GOTO 1000
C
C     OUTPUT BLOCK MESH FILE
      CALL OUTPUT_BlockMeshDict
C
      STOP
C
 1000 CALL BLANK
      CALL MESSAGE('     **** EXITING PROGRM ****')
C
      END PROGRAM
